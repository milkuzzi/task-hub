/**
 * Чистая логика решения скользящего окна для ограничения частоты запросов
 * (Req 19.1, 19.2).
 *
 * Логика отделена от инфраструктуры (Redis), чтобы её можно было детерминированно
 * проверять модульными и property-тестами (свойство 55, задача 17.2). Реализация
 * {@link RateLimiter} лишь хранит метки времени в Redis и делегирует само решение
 * этой функции.
 */

/** Решение скользящего окна для очередного запроса. */
export interface SlidingWindowDecision {
  /**
   * Допущен ли текущий запрос: допускается, если в пределах окна уже
   * зафиксировано строго меньше {@link maxRequests} запросов.
   */
  allowed: boolean;
  /**
   * Набор меток времени, который следует хранить после обработки запроса:
   * метки в пределах окна плюс метка текущего запроса, если он допущен.
   * Отклонённые запросы не добавляются и не продлевают окно.
   */
  retained: number[];
}

/**
 * Вычисляет решение скользящего окна для запроса, поступившего в момент `now`.
 *
 * Из набора ранее зафиксированных меток времени `existing` сохраняются только
 * те, что попадают в окно `(now - windowMs, now]` (запросы строго старше
 * границы окна считаются истёкшими). Запрос допускается, если число оставшихся
 * меток строго меньше `maxRequests`; в этом случае метка `now` добавляется к
 * сохранённым. При превышении лимита запрос отклоняется, а его метка не
 * сохраняется (Req 19.1, 19.2).
 *
 * @param existing Ранее зафиксированные метки времени (мс эпохи).
 * @param now Момент текущего запроса (мс эпохи).
 * @param windowMs Длительность скользящего окна в миллисекундах (> 0).
 * @param maxRequests Максимально допустимое число запросов в окне (≥ 0).
 */
export function evaluateSlidingWindow(
  existing: readonly number[],
  now: number,
  windowMs: number,
  maxRequests: number,
): SlidingWindowDecision {
  const windowStart = now - windowMs;
  // Истёкшими считаются метки со временем ≤ начала окна; остаются строго свежее.
  const valid = existing.filter((timestamp) => timestamp > windowStart);
  const allowed = valid.length < maxRequests;
  return {
    allowed,
    retained: allowed ? [...valid, now] : valid,
  };
}
