import { NotificationType, Prisma } from '@prisma/client';

/**
 * Канал доставки уведомления (Req 13.2, 13.13, 14.6).
 *
 * Уведомление доставляется на сайт и через Бот MAX; доставка на сайт
 * сохраняется независимо от результата доставки в MAX (Req 14.6, 15.7).
 */
export enum NotificationChannel {
  /** Доставка на сайт (Socket.IO / центр уведомлений). */
  Site = 'site',
  /** Доставка через Бот MAX. */
  Max = 'max',
}

/**
 * Каналы доставки по умолчанию: сайт + MAX (Req 13.2).
 */
export const DEFAULT_NOTIFICATION_CHANNELS: readonly NotificationChannel[] = [
  NotificationChannel.Site,
  NotificationChannel.Max,
];

/**
 * Доменное событие, требующее формирования уведомлений (Req 13.1, 13.12).
 *
 * Обобщённое описание события предметной области: маршрутизация конкретных
 * событий Задачи/Чата в это представление выполняется вызывающими модулями
 * (задачи 12.3 / 12.7). Поле {@link eventKey} — стабильный ключ события,
 * используемый для идемпотентности: повторный вызов
 * {@link import('./notifications.service').NotificationsService.emit} с тем же
 * ключом не создаёт дублирующих уведомлений (Req 13.1).
 */
export interface DomainEvent {
  /** Тип уведомления (определяет шаблон/маршрутизацию). */
  type: NotificationType;
  /**
   * Идентификаторы получателей. Для каждого получателя создаётся ОТДЕЛЬНОЕ
   * уведомление без объединения в дайджест (Req 13.1). Повторяющиеся
   * идентификаторы дедуплицируются.
   */
  recipientIds: readonly string[];
  /** Идентификатор связанной Задачи (если применимо). */
  taskId?: string | null;
  /** Идентификатор связанного Сообщения (для уведомлений о сообщениях, Req 14.4). */
  messageId?: string | null;
  /** Произвольная полезная нагрузка уведомления (новый статус, имена и т. п.). */
  payload: Prisma.InputJsonValue;
  /**
   * Признак уведомления о сообщении Чата: такие уведомления удаляются по факту
   * просмотра (Req 14.4, 14.5). По умолчанию `false`.
   */
  isMessageNotification?: boolean;
  /**
   * Стабильный ключ события для идемпотентности (Req 13.1). Одинаков для
   * повторных формирований одного и того же события; уникален для разных
   * событий. Маркер захватывается отдельно на каждого получателя.
   */
  eventKey: string;
  /**
   * Каналы доставки. По умолчанию — сайт + MAX
   * ({@link DEFAULT_NOTIFICATION_CHANNELS}).
   */
  channels?: readonly NotificationChannel[];
}

/**
 * Данные задания доставки уведомления, помещаемого в очередь BullMQ (Req 13.12).
 *
 * Содержит идентификатор созданной записи уведомления, получателя и каналы
 * доставки. Фактическая доставка по каналам и ретраи выполняются воркером
 * доставки (задача 12.10).
 */
export interface NotificationDeliveryJobData {
  /** Идентификатор созданной записи уведомления. */
  notificationId: string;
  /** Идентификатор получателя уведомления. */
  recipientId: string;
  /** Каналы, по которым требуется доставить уведомление. */
  channels: NotificationChannel[];
}
