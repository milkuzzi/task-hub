import { Injectable } from '@nestjs/common';

/**
 * Токен инъекции порта генератора миниатюр (Req 12.6).
 *
 * Использование символа-токена позволяет подменять реализацию генерации
 * миниатюр без изменения {@link AttachmentsService} и упрощает её подмену в
 * тестах. В частности, реальное масштабирование изображений (например, на базе
 * `sharp`) подключается как альтернативная реализация {@link ThumbnailGenerator}
 * через провайдер модуля, не требуя нативных зависимостей в основном коде и
 * тестах.
 */
export const THUMBNAIL_GENERATOR = Symbol('THUMBNAIL_GENERATOR');

/** Исходные данные для формирования миниатюры. */
export interface ThumbnailInput {
  /** MIME-тип исходного Вложения (например, `image/png`). */
  mimeType: string;
  /** Распакованное (исходное) содержимое изображения. */
  content: Buffer;
}

/**
 * Порт генератора миниатюр изображений (Req 12.6).
 *
 * Инкапсулирует формирование уменьшенного представления изображения.
 * {@link AttachmentsService} принимает решение о применимости миниатюры
 * (изображение в пределах лимита) через чистый селектор представления и лишь
 * затем делегирует фактическую генерацию байтов миниатюры этому порту, а
 * результат сохраняет в хранилище.
 *
 * Решение по реализации: тяжёлая нативная библиотека обработки изображений
 * (`sharp`) в проект не добавлена, поэтому генерация скрыта за этим портом.
 * Реализация по умолчанию ({@link PassthroughThumbnailGenerator}) не имеет
 * внешних зависимостей и пригодна для тестов; продуктовая реализация с реальным
 * масштабированием подключается заменой провайдера токена
 * {@link THUMBNAIL_GENERATOR} без изменения сервиса.
 */
export interface ThumbnailGenerator {
  /**
   * Формирует байты миниатюры для переданного изображения.
   * @param input Исходное изображение (MIME-тип и распакованное содержимое).
   * @returns Байты миниатюры для сохранения в хранилище.
   */
  generate(input: ThumbnailInput): Promise<Buffer>;
}

/**
 * Реализация генератора миниатюр по умолчанию, не требующая нативных
 * зависимостей.
 *
 * Поскольку библиотека масштабирования изображений в проект не подключена,
 * реализация возвращает исходные байты изображения без потерь как
 * полноразмерное представление-миниатюру. Это сохраняет корректность контракта
 * ({@link AttachmentsService} получает валидные байты миниатюры и записывает
 * `Attachment.thumbnailPath`) и делает генерацию полностью тестируемой без
 * нативных библиотек. Для настоящего уменьшения размера достаточно подменить
 * провайдер {@link THUMBNAIL_GENERATOR} реализацией на базе `sharp`.
 */
@Injectable()
export class PassthroughThumbnailGenerator implements ThumbnailGenerator {
  async generate(input: ThumbnailInput): Promise<Buffer> {
    return input.content;
  }
}
