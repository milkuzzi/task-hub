import { Readable } from 'node:stream';
import { Attachment } from '@prisma/client';
import { GenericIconType } from './attachment-representation';

/**
 * Типы загрузки Вложений в Чат Задачи (Req 11.9, 12.1–12.5, 19.8, 19.9).
 *
 * Загрузка выполняется в контексте конкретного Сообщения Чата: содержимое
 * сжимается и сохраняется вне веб-корня через {@link StorageService}, после
 * чего создаётся запись Вложения. Лимиты (размер ≤ 25 МБ и ≤ 10 Вложений на
 * Сообщение) проверяются до сохранения; при их нарушении или при сбое передачи
 * частичный файл не сохраняется и запись не создаётся (Req 12.3, 12.4, 19.9).
 */

/**
 * Загружаемый файл произвольного типа (Req 12.1, 12.5).
 *
 * Содержимое может быть как буфером (когда файл уже целиком в памяти), так и
 * потоком (`Readable`) для потокового сжатия больших файлов без полной
 * буферизации. Поле {@link declaredSize} используется для быстрого отклонения
 * заведомо превышающих лимит файлов до начала чтения; фактический размер
 * исходного содержимого дополнительно проверяется после сохранения.
 */
export interface UploadFile {
  /** Исходное имя файла, отображаемое Участникам чата. */
  originalName: string;
  /**
   * MIME-тип файла. Разрешены любые типы (Req 12.5); значение используется лишь
   * для выбора представления (миниатюра/значок) на последующих этапах.
   */
  mimeType: string;
  /**
   * Заявленный размер файла в байтах (например, из заголовка `Content-Length`
   * или метаданных формы), если известен. Позволяет отклонить превышающий лимит
   * файл до чтения содержимого (Req 12.3).
   */
  declaredSize?: number;
  /** Содержимое файла: буфер целиком либо поток для потокового сохранения. */
  content: Buffer | Readable;
}

/**
 * Сжатый поток сохранённого Вложения для контролируемой отдачи (Req 12.8, 12.9,
 * 19.8).
 *
 * Возвращается при открытии Вложения-изображения по миниатюре для
 * полноэкранного просмотра: сервер отдаёт сжатые байты, а распаковка без потерь
 * выполняется на стороне клиента (Req 12.9). Сопутствующие метаданные позволяют
 * клиенту корректно интерпретировать поток (тип, кодек) и проверить целостность
 * (контрольная сумма исходного содержимого).
 */
export interface CompressedStream {
  /** Сжатый поток байтов сохранённого объекта. */
  stream: Readable;
  /** MIME-тип исходного содержимого Вложения. */
  mimeType: string;
  /** Исходное имя файла Вложения. */
  originalName: string;
  /** Кодек сжатия, которым сжат поток (например, `zstd`). */
  compression: string;
  /** Размер исходного (несжатого) содержимого в байтах. */
  sizeBytes: number;
  /** Контрольная сумма (sha256, hex) исходного содержимого для проверки на клиенте. */
  checksum: string;
}

/**
 * Готовый PDF-предпросмотр офисного Вложения.
 *
 * В отличие от клиентского разбора ячеек, этот результат формируется рендером
 * исходного файла в PDF и сохраняет внешний вид документа или презентации
 * настолько, насколько его воспроизводит LibreOffice.
 */
export interface DocumentPreview {
  /** Байты PDF-документа для inline-показа. */
  content: Buffer;
  /** MIME-тип предпросмотра. */
  mimeType: 'application/pdf';
  /** Имя файла предпросмотра для заголовка Content-Disposition. */
  fileName: string;
}

/**
 * Результат загрузки Вложения в Чат Задачи до отправки Сообщения (Req 12.1–12.5).
 *
 * Несёт созданную запись Вложения и момент его загрузки (`createdAt`) для
 * формирования контракта `AttachmentMeta`. Момент берётся из собственного поля
 * `Attachment.createdAt` — Вложение существует «висящим» (непривязанным) до
 * отправки Сообщения, к которому будет привязано.
 */
export interface UploadedAttachment {
  /** Созданная запись Вложения. */
  attachment: Attachment;
  /** Момент загрузки Вложения (`Attachment.createdAt`). */
  createdAt: Date;
}

/**
 * Представление Вложения для отдачи миниатюры (Req 12.6, 12.7).
 *
 * Для изображений отдаётся распакованное содержимое миниатюры (готовое к показу
 * в теге `<img>`); для прочих типов — обобщённый значок, соответствующий типу
 * файла, который клиент отображает самостоятельно (Req 12.7).
 */
export type ThumbnailResult =
  | {
      /** Готовое к показу изображение-миниатюра (распакованное). */
      kind: 'image';
      /** Распакованные байты изображения. */
      content: Buffer;
      /** MIME-тип изображения. */
      mimeType: string;
    }
  | {
      /** Обобщённый значок по типу файла (изображения нет). */
      kind: 'icon';
      /** Категория значка. */
      icon: GenericIconType;
    };
