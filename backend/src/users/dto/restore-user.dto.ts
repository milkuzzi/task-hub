import { IsEmail } from 'class-validator';

/**
 * DTO восстановления удалённого Пользователя по выбранному сохранённому адресу
 * электронной почты (Req 7.2, 7.3).
 *
 * Принадлежность адреса истории Пользователя и отсутствие конфликта с активной
 * учётной записью проверяются в
 * {@link import('../users.service').UsersService.restoreUser}.
 */
export class RestoreUserDto {
  /** Выбранный сохранённый адрес электронной почты для повторной регистрации. */
  @IsEmail({}, { message: 'Адрес электронной почты имеет недопустимый формат.' })
  email!: string;
}
