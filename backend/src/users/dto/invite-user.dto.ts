import { IsEmail } from 'class-validator';

/**
 * DTO приглашения нового Пользователя по адресу электронной почты (Req 5.1–5.3).
 *
 * Применяется глобальным `ValidationPipe`; формат адреса дополнительно
 * проверяется в {@link import('../../auth').AuthService.invite}. При нарушении
 * глобальный фильтр исключений возвращает единый формат `{ code, message }`
 * (Req 1.1).
 */
export class InviteUserDto {
  /** Адрес электронной почты приглашаемого Пользователя. */
  @IsEmail({}, { message: 'Адрес электронной почты имеет недопустимый формат.' })
  email!: string;
}
