/**
 * Типы предметной области для управления профилем пользователя (Req 6).
 *
 * Вынесены в отдельный модуль без зависимостей от NestJS/Prisma, чтобы
 * упростить переиспользование в прикладном сервисе, контроллерах и тестах.
 */

/**
 * Набор изменяемых Администратором полей профиля (Req 6.2, 6.3).
 *
 * Каждое поле необязательно: отсутствие поля означает «не изменять». Изменение
 * адреса электронной почты и отображаемого имени разрешено только
 * Администратору (Req 6.2, 6.3, 6.8).
 */
export interface ProfilePatch {
  /** Новый адрес электронной почты пользователя (только Администратор, Req 6.2). */
  email?: string;
  /** Новое отображаемое имя пользователя (только Администратор, Req 6.3). */
  displayName?: string;
}

/**
 * Типизированное представление загруженного файла аватара (Req 6.4, 6.5, 6.9).
 *
 * Фактическое хранение делегируется {@link AvatarStorage}; здесь описаны лишь
 * метаданные и содержимое, необходимые для валидации и сохранения.
 */
export interface UploadedFile {
  /** Исходное имя файла (для формирования пути хранения и расширения). */
  originalName: string;
  /** MIME-тип файла (проверяется на поддерживаемый растровый формат). */
  mimeType: string;
  /** Размер файла в байтах (проверяется против лимита 5 МБ, Req 6.4, 6.5). */
  sizeBytes: number;
  /** Необязательное содержимое файла (поток/буфер) для передачи в хранилище. */
  buffer?: Buffer;
}

/**
 * Профиль MAX, передаваемый для привязки к учётной записи (Req 6.6, 6.9, 16.2).
 *
 * Привязка MAX является дополнением и не заменяет регистрацию Администратором
 * (Req 16.2). Профиль считается пригодным к привязке только при успешной
 * верификации на стороне MAX (`verified = true`).
 */
export interface MaxProfile {
  /** Идентификатор профиля на стороне платформы MAX. */
  maxUserId: string;
  /**
   * Признак успешной верификации профиля на стороне MAX (OAuth). Если `false`,
   * привязка считается неуспешной и отклоняется (Req 6.9).
   */
  verified: boolean;
  /**
   * Системный пользователь, которому фактически принадлежит профиль, если это
   * известно из контекста верификации. При несовпадении с целевым
   * пользователем привязка трактуется как попытка привязать чужой профиль и
   * отклоняется (Req 6.9).
   */
  ownerUserId?: string;
}
