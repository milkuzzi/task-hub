/**
 * Типы конечного автомата статусов Задачи (Req 10).
 *
 * Эти типы описывают предметную область автомата без какой-либо привязки к
 * инфраструктуре: они используются как при ручной смене статуса
 * (TasksModule), так и при авто-переключении из чата (ChatModule).
 */

/** Статус Задачи — состояние конечного автомата (Req 10). */
export type Status = 'IN_PROGRESS' | 'WAITING' | 'DONE' | 'NEEDS_ADMIN' | 'CANCELLED';

/**
 * Роль действующего лица в контексте Задачи.
 *
 * Менеджер, назначенный Исполнителем Задачи, получает права Исполнителя
 * (Req 2.4); решение о том, какую роль передать в автомат, принимает
 * вызывающая сторона, а сам автомат оперирует уже разрешённой ролью.
 */
export type Actor = 'EXECUTOR' | 'MANAGER' | 'ADMIN';

/**
 * Явное действие смены статуса, инициированное Пользователем (Req 10.4–10.10).
 *
 * Действие `ADMIN_SET` несёт целевой статус, выбранный Администратором из
 * Статуса «Требует администратора» (Req 10.9).
 */
export type StatusAction =
  | { type: 'COMPLETE' } // Пометить «Выполнено» (Req 10.4)
  | { type: 'REOPEN' } // Переоткрыть из «Выполнено» (Req 10.5)
  | { type: 'CANCEL' } // Отменить (Req 10.6)
  | { type: 'RETURN' } // Вернуть из «Отменено» (Req 10.7)
  | { type: 'REQUEST_ADMIN' } // Запросить «Требует администратора» (Req 10.8)
  | { type: 'ADMIN_SET'; target: Status } // Администратор выбирает статус (Req 10.9)
  | { type: 'CLEAR_ADMIN' }; // Менеджер снимает «Требует администратора» без проверки (Req 10.10)

/** Причина отказа в переходе. */
export type TransitionError = 'NO_PERMISSION' | 'INVALID_TRANSITION';

/**
 * Результат явного перехода: либо новый статус, либо отказ
 * (Req 10.14 — нет прав; Req 10.15 — недопустимый переход).
 */
export type TransitionResult = { status: Status } | { error: TransitionError };
