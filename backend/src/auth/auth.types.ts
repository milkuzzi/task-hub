import { Role } from '@prisma/client';

/**
 * Полезная нагрузка JWT access-токена.
 *
 * `sub` — идентификатор пользователя, `jti` — идентификатор токена (он же ключ
 * записи сессии в реестре Redis), `role` — роль на момент выпуска токена.
 * Подпись и срок действия проверяются библиотекой JWT, а валидность сессии
 * (не аннулирована ли токен) — по реестру сессий (Req 5.7, 19.10).
 */
export interface AccessTokenPayload {
  /** Идентификатор пользователя (subject). */
  sub: string;
  /** Идентификатор токена/сессии (JWT ID). */
  jti: string;
  /** Роль пользователя на момент выпуска токена. */
  role: Role;
}

/**
 * Аутентифицированный субъект запроса/socket-подключения.
 *
 * Формируется после успешной проверки токена и валидности сессии. Используется
 * как `request.user` HTTP-guard-ом и Gateway при подключении сокета.
 */
export interface AuthPrincipal {
  /** Идентификатор пользователя. */
  userId: string;
  /** Идентификатор активной сессии/токена. */
  tokenId: string;
  /** Роль пользователя. */
  role: Role;
}

/**
 * Результат успешного входа (Req 5.7).
 *
 * Содержит выпущенный access-токен и метаданные созданной сессии. Соответствует
 * доменной сущности `Session`: владелец, идентификатор токена и момент
 * истечения.
 */
export interface AuthSession {
  /** Подписанный JWT access-токен для заголовка `Authorization: Bearer`. */
  accessToken: string;
  /** Идентификатор токена/сессии (jti) — ключ записи в реестре сессий. */
  tokenId: string;
  /** Идентификатор пользователя — владельца сессии. */
  userId: string;
  /** Роль пользователя на момент входа. */
  role: Role;
  /** Момент истечения сессии (абсолютный, UTC). */
  expiresAt: Date;
}
