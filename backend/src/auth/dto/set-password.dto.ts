import { IsString } from 'class-validator';

/**
 * DTO установки пароля по одноразовой ссылке (активация учётной записи,
 * Req 5.5, 6.7).
 *
 * Содержит секрет токена из ссылки письма и новый пароль. Длина пароля
 * проверяется в {@link import('../auth.service').AuthService.setPassword} до
 * обращения к токену, чтобы не «сжигать» действующую ссылку из-за некорректного
 * ввода (Req 6.7).
 */
export class SetPasswordDto {
  /** Открытый секрет токена установки пароля из ссылки письма. */
  @IsString({ message: 'Токен должен быть строкой.' })
  token!: string;

  /** Новый пароль в открытом виде. */
  @IsString({ message: 'Пароль должен быть строкой.' })
  password!: string;
}
