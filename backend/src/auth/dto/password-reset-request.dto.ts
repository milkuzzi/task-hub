import { IsEmail } from 'class-validator';

/**
 * DTO запроса ссылки восстановления пароля.
 *
 * По соображениям безопасности ответ маршрута одинаковый для существующего и
 * неизвестного адреса: наличие учётной записи не раскрывается публичному
 * пользователю.
 */
export class PasswordResetRequestDto {
  /** Адрес электронной почты Пользователя. */
  @IsEmail({}, { message: 'Адрес электронной почты имеет недопустимый формат.' })
  email!: string;
}
