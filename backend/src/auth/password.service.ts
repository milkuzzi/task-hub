import { Injectable } from '@nestjs/common';
import * as bcrypt from 'bcrypt';

/**
 * Число раундов соли bcrypt (cost factor).
 *
 * Значение 12 — общепринятый компромисс между стойкостью к перебору и временем
 * вычисления хеша для интерактивных операций входа/установки пароля. Хранится
 * как константа, а не в конфигурации, чтобы исключить случайное ослабление
 * параметра в окружении (Req 19 — безопасность учётных данных).
 */
const BCRYPT_ROUNDS = 12;

/**
 * Сервис хеширования и проверки паролей.
 *
 * Использует проверенный алгоритм bcrypt (с автоматической солью): пароли
 * никогда не хранятся и не сравниваются в открытом виде. Инкапсулирует выбор
 * алгоритма и его параметры, чтобы остальной код зависел только от абстрактных
 * операций «захешировать»/«проверить» (Req 5.5, 6.7).
 */
@Injectable()
export class PasswordService {
  /**
   * Вычисляет необратимый хеш пароля с уникальной солью.
   * @param plain Пароль в открытом виде.
   * @returns Строка хеша bcrypt, пригодная для хранения в `User.passwordHash`.
   */
  hash(plain: string): Promise<string> {
    return bcrypt.hash(plain, BCRYPT_ROUNDS);
  }

  /**
   * Проверяет соответствие открытого пароля сохранённому хешу.
   * @param plain Пароль в открытом виде.
   * @param hash Ранее сохранённый хеш bcrypt.
   * @returns `true`, если пароль соответствует хешу, иначе `false`.
   */
  verify(plain: string, hash: string): Promise<boolean> {
    return bcrypt.compare(plain, hash);
  }
}
