/**
 * Единый формат ответа об ошибке API (Req 1.1).
 *
 * Все ошибочные ответы Системы возвращаются клиенту в виде структуры
 * `{ code, message, details? }`, где:
 * - `code` — стабильный машиночитаемый код, независимый от языка интерфейса;
 * - `message` — локализованный русский текст для отображения пользователю;
 * - `details` — необязательная дополнительная информация (например, список
 *   нарушений валидации по полям). Не должна раскрывать содержимое ресурсов,
 *   к которым у пользователя нет доступа (Req 2.12).
 */
export interface ErrorResponseBody {
  /** Стабильный код ошибки, не зависящий от языка (например, `ACCESS_DENIED`). */
  code: string;
  /** Локализованное русское сообщение об ошибке (Req 1.1). */
  message: string;
  /** Необязательные дополнительные сведения об ошибке. */
  details?: unknown;
}

/**
 * Собирает тело ответа об ошибке в едином формате.
 *
 * Поле `details` включается в результат только тогда, когда оно задано, что
 * соответствует строгой настройке `exactOptionalPropertyTypes` в TypeScript.
 */
export function buildErrorResponse(
  code: string,
  message: string,
  details?: unknown,
): ErrorResponseBody {
  return details === undefined ? { code, message } : { code, message, details };
}
