/**
 * Единый конверт успешного ответа API.
 *
 * Дополняет единый формат ошибок (`{ code, message, details? }`, Req 1.1):
 * успешные ответы возвращают полезную нагрузку в поле `data`, что даёт клиенту
 * предсказуемую форму как для успеха, так и для ошибки.
 *
 * @typeParam T Тип полезной нагрузки ответа.
 */
export interface ApiResponse<T> {
  /** Полезная нагрузка успешного ответа. */
  data: T;
}

/**
 * Оборачивает полезную нагрузку в единый конверт успешного ответа.
 *
 * @param data Данные, возвращаемые клиенту.
 */
export function ok<T>(data: T): ApiResponse<T> {
  return { data };
}
