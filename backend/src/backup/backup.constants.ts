/**
 * Константы модуля резервного копирования (Req 21).
 *
 * Фиксируют расписание ежедневного запуска (03:00 MSK), идентификаторы
 * повторяющегося задания BullMQ и предельную длительность выполнения (60 минут).
 */

/** Имя задания ежедневного резервного копирования. */
export const BACKUP_DAILY_JOB_NAME = 'run-daily-backup';

/**
 * Идентификатор повторяющегося задания ежедневного бэкапа.
 * Стабильный `jobId` защищает от дублирования расписания при перезапусках.
 */
export const BACKUP_DAILY_JOB_ID = 'backup-daily';

/**
 * Cron-расписание ежедневного запуска в 03:00 (Req 21.2).
 * Интерпретируется в часовом поясе {@link BACKUP_TIMEZONE}.
 */
export const BACKUP_CRON_PATTERN = '0 3 * * *';

/**
 * Часовой пояс расписания резервного копирования — Москва (MSK = UTC+3).
 * Все даты и время Системы используют MSK (Req 1.2), поэтому ежедневный запуск
 * привязан к настенному времени Москвы, а не к UTC.
 */
export const BACKUP_TIMEZONE = 'Europe/Moscow';

/**
 * Предельная длительность одного выполнения резервного копирования — 60 минут
 * (Req 21.2, 21.8). Если дамп БД и выгрузка в S3 не укладываются в это окно,
 * выполнение пропускается, а последняя успешная копия сохраняется без изменений.
 */
export const BACKUP_MAX_DURATION_MS = 60 * 60 * 1000;
