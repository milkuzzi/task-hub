import { BackupResult } from '@prisma/client';

/**
 * Типы и порты модуля резервного копирования (Req 21).
 *
 * Внешние границы (инструмент дедупликации restic и S3-совместимое хранилище)
 * вынесены в порты, чтобы прикладную логику запуска ежедневного бэкапа
 * ({@link import('./backup.service').BackupService.runDailyBackup}) можно было
 * реализовать и протестировать независимо от готовности инфраструктуры и без
 * фактического обращения к диску/сети (см. стратегию тестирования дизайна).
 */

/**
 * Результат создания дампа БД инструментом restic (Req 21.1, 21.2).
 *
 * Контрольная сумма вычисляется до выгрузки и используется при последующей
 * проверке целостности (Req 21.6, задача 18.5).
 */
export interface DatabaseDumpResult {
  /** Контрольная сумма созданного дампа, вычисленная до выгрузки (Req 21.6). */
  checksum: string;
  /** Идентификатор снимка restic (для диагностики и последующих задач). */
  snapshotId?: string;
  /** Размер дампа в байтах (необязательно, для диагностики). */
  sizeBytes?: number;
}

/**
 * Структурированный итог одного запуска {@link
 * import('./backup.service').BackupService.runDailyBackup}.
 *
 * Соответствует объекту `BackupResult` из дизайна (`BackupService.runDailyBackup(): Promise<BackupResult>`).
 * Назван `BackupRunResult`, чтобы не конфликтовать с одноимённым перечислением
 * Prisma {@link BackupResult} (`SUCCESS|FAILED|SKIPPED|INTEGRITY_ERROR`),
 * используемым здесь для поля {@link BackupRunResult.result}.
 */
export interface BackupRunResult {
  /** Идентификатор созданной записи журнала бэкапа {@link BackupRecord}. */
  recordId: string;
  /** Итог выполнения (Req 21.2, 21.4, 21.5, 21.8). */
  result: BackupResult;
  /** Момент начала выполнения (UTC). */
  startedAt: Date;
  /** Момент завершения выполнения (UTC). */
  finishedAt: Date;
  /** Контрольная сумма копии при успехе (Req 21.6). */
  checksum?: string;
  /** Причина пропуска/сбоя при неуспехе (Req 21.5, 21.8). */
  reason?: string;
}

/**
 * Порт инструмента резервного копирования с дедупликацией (restic, Req 21.1,
 * 21.2).
 *
 * Абстрагирует создание дампа БД и его помещение в дедуплицируемый репозиторий
 * restic (дедупликация/сжатие/инкрементальность). Реальная реализация
 * (вызов restic) подключается на уровне инфраструктуры/развёртывания; в тестах
 * и до подключения используется управляемая заглушка.
 */
export interface ResticBackupPort {
  /**
   * Создаёт дамп БД и помещает его в дедуплицируемый репозиторий restic.
   *
   * @param signal Сигнал отмены: прерывается при превышении предельной
   *   длительности выполнения (Req 21.8).
   * @returns Сведения о созданном дампе, включая контрольную сумму.
   * @throws Error при сбое создания дампа (Req 21.5).
   */
  createDump(signal?: AbortSignal): Promise<DatabaseDumpResult>;
}

/**
 * Порт выгрузки резервной копии вне VPS в S3-совместимое хранилище (Req 21.4).
 *
 * Реальная реализация (S3-клиент) подключается на уровне инфраструктуры; в
 * тестах и до подключения используется управляемая заглушка.
 */
export interface OffsiteUploadPort {
  /**
   * Выгружает дамп вне VPS в S3-совместимое хранилище.
   *
   * @param dump Сведения о созданном дампе.
   * @param signal Сигнал отмены при превышении предельной длительности (Req 21.8).
   * @throws Error при сбое выгрузки (Req 21.5).
   */
  upload(dump: DatabaseDumpResult, signal?: AbortSignal): Promise<void>;

  /**
   * Вычисляет контрольную сумму резервной копии, уже выгруженной в
   * S3-совместимое хранилище (Req 21.6).
   *
   * Используется при проверке целостности
   * ({@link import('./backup.service').BackupService.verifyIntegrity}): сумма
   * выгруженной копии сверяется с суммой, вычисленной до выгрузки. Реальный
   * адаптер сопоставляет копию по {@link reference} (контрольная сумма,
   * вычисленная до выгрузки) и пересчитывает её фактическую сумму в хранилище.
   *
   * @param reference Сведения о выгруженной копии (как минимум — контрольная
   *   сумма, зафиксированная до выгрузки), позволяющие адаптеру найти объект в
   *   хранилище.
   * @param signal Сигнал отмены.
   * @returns Фактическая контрольная сумма копии в хранилище.
   * @throws Error если копию не удалось прочитать из хранилища.
   */
  computeUploadedChecksum(
    reference: UploadedBackupReference,
    signal?: AbortSignal,
  ): Promise<string>;
}

/**
 * Ссылка на резервную копию, выгруженную в S3-совместимое хранилище, для
 * последующей проверки целостности (Req 21.6).
 *
 * Содержит контрольную сумму, вычисленную до выгрузки, по которой реальный
 * адаптер хранилища находит соответствующий объект и пересчитывает его
 * фактическую сумму.
 */
export interface UploadedBackupReference {
  /** Идентификатор записи журнала резервной копии {@link BackupRecord}. */
  backupId: string;
  /** Контрольная сумма копии, вычисленная до выгрузки (Req 21.6). */
  checksum: string;
}

/** DI-токен порта {@link ResticBackupPort}. */
export const RESTIC_BACKUP_PORT = Symbol('RESTIC_BACKUP_PORT');

/** DI-токен порта {@link OffsiteUploadPort}. */
export const OFFSITE_UPLOAD_PORT = Symbol('OFFSITE_UPLOAD_PORT');
