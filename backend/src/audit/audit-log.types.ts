/**
 * Запись Журнала изменений в представлении для отображения (Req 20.1, 20.2).
 *
 * Повторяет поля хранимой записи {@link import('@prisma/client').AuditEntry} и
 * дополнительно содержит момент изменения, отформатированный в часовом поясе
 * Москвы (`ДД.ММ.ГГГГ ЧЧ:ММ`). Время хранится в UTC ({@link changedAt}); поле
 * {@link changedAtMsk} — производное представление для интерфейса (Req 20.1).
 */
export interface AuditLogEntryView {
  /** Идентификатор записи Журнала. */
  id: string;
  /** Идентификатор Задачи, к которой относится изменение. */
  taskId: string;
  /** Идентификатор автора изменения; `null`, если автор удалён (hard-delete). */
  authorId: string | null;
  /** Машинное имя изменённого параметра (`title`, `description`, `deadline`, …). */
  field: string;
  /** Прежнее значение параметра в строковом представлении (`null`, если не было). */
  oldValue: string | null;
  /** Новое значение параметра в строковом представлении (`null`, если очищено). */
  newValue: string | null;
  /** Момент изменения (абсолютный, UTC). */
  changedAt: Date;
  /** Момент изменения в MSK для отображения, формат `ДД.ММ.ГГГГ ЧЧ:ММ` (Req 20.1). */
  changedAtMsk: string;
}
