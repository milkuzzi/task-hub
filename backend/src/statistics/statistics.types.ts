import { TaskStatus } from '@prisma/client';

/**
 * Типы модуля Статистики для Администратора (Req 17).
 *
 * Описывают предметную область расчёта статистики без привязки к
 * инфраструктуре: облегчённые записи задач/сообщений, на которых работают
 * чистые функции расчёта ({@link ./statistics.math}), период выборки и итоговый
 * результат {@link Statistics}, отображаемый Администратору.
 */

/**
 * Период выборки статистики, задаваемый Администратором датами начала и
 * окончания (Req 17.6). Границы трактуются включительно. Моменты абсолютны
 * (UTC); представление в MSK — задача слоя отображения.
 */
export interface DateRange {
  /** Начало периода (включительно). */
  start: Date;
  /** Конец периода (включительно). */
  end: Date;
}

/**
 * Облегчённая запись Задачи для расчёта статистики.
 *
 * Содержит ровно те поля, которые нужны чистым функциям расчёта: статус,
 * Дедлайн и моменты создания/завершения (для просрочек и среднего времени,
 * Req 17.2, 17.3) и идентификаторы назначенных Менеджеров/Исполнителей (для
 * разрезов по участникам, Req 17.4).
 */
export interface StatTaskRecord {
  /** Текущий Статус Задачи. */
  status: TaskStatus;
  /** Дедлайн Задачи (UTC). */
  deadline: Date;
  /** Момент создания Задачи (UTC). */
  createdAt: Date;
  /** Момент перехода в Статус «Выполнено» (UTC); `null`, если не выполнена. */
  doneAt: Date | null;
  /** Идентификаторы назначенных Исполнителей. */
  executorIds: string[];
  /** Идентификаторы назначенных Менеджеров. */
  managerIds: string[];
}

/**
 * Облегчённая запись Сообщения для расчёта активности Чатов (Req 17.5).
 * Достаточно идентификатора Чата: общее число Сообщений — это количество
 * записей, активные Чаты — число различных `chatId`.
 */
export interface StatMessageRecord {
  /** Идентификатор Чата, которому принадлежит Сообщение. */
  chatId: string;
}

/** Показатели активности Чатов (Req 17.5). */
export interface ChatActivity {
  /** Общее количество отправленных Сообщений за период. */
  totalMessages: number;
  /** Количество Чатов, в которых было хотя бы одно Сообщение. */
  activeChats: number;
}

/**
 * Итоговая статистика, отображаемая Администратору (Req 17.1–17.5, 17.8).
 *
 * Все числовые показатели уже округлены согласно требованиям (доля просроченных
 * и среднее время — до одного знака после запятой). При отсутствии данных за
 * период все показатели нулевые, а {@link noData} равен `true` (Req 17.8).
 */
export interface Statistics {
  /** Количество Задач по каждому Статусу, включая нулевые (Req 17.1). */
  byStatus: Record<TaskStatus, number>;
  /** Общее количество Задач за период. */
  totalTasks: number;
  /** Количество просроченных Задач (Req 17.2). */
  overdueCount: number;
  /** Доля просроченных Задач в процентах, округлённая до 1 знака (Req 17.2). */
  overduePercent: number;
  /** Среднее время выполнения в часах, округлённое до 1 знака; 0 при отсутствии (Req 17.3). */
  averageCompletionHours: number;
  /** Количество Задач в разрезе каждого Менеджера (Req 17.4). */
  byManager: Record<string, number>;
  /** Количество Задач в разрезе каждого Исполнителя (Req 17.4). */
  byExecutor: Record<string, number>;
  /** Показатели активности Чатов (Req 17.5). */
  chatActivity: ChatActivity;
  /** Период выборки; `null`, если период не задавался. */
  period: DateRange | null;
  /** Признак отсутствия данных за выбранный период (Req 17.8). */
  noData: boolean;
}

/**
 * Полный набор исходных данных для расчёта статистики — результат выборки из
 * репозитория, передаваемый в чистую функцию расчёта.
 */
export interface StatisticsInput {
  /** Задачи, попадающие в период (по моменту создания). */
  tasks: StatTaskRecord[];
  /** Сообщения, попадающие в период (по моменту создания). */
  messages: StatMessageRecord[];
  /** Период выборки (или `null`). */
  period: DateRange | null;
  /** Текущий момент времени для классификации просрочек (Req 17.2). */
  now: Date;
}
