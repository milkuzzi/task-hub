/**
 * Чистая логика счётчика количества Сообщений на карточке Задачи (Req 9.7, 9.9).
 *
 * Отображаемый счётчик представляет собой целое число в диапазоне 0–`cap`
 * (по умолчанию потолок равен 9999, см. {@link LimitsConfig.messageCounterCap}).
 * При фактическом числе Сообщений, равном `cap` или большем (т.е. ≥10000 при
 * стандартном потолке), счётчик насыщается и фиксируется на значении `cap`,
 * прекращая дальнейшее увеличение отображаемого значения (Req 9.9).
 *
 * Функция полностью детерминирована и не имеет побочных эффектов, что позволяет
 * проверять её свойством насыщения (Property 24) в изоляции от хранилища.
 *
 * @param actualCount Фактическое число Сообщений в Чате Задачи. Дробная часть
 *   отбрасывается; отрицательные значения считаются нулём (счётчик не может
 *   быть отрицательным).
 * @param cap Верхняя граница отображаемого счётчика (потолок насыщения).
 * @returns Отображаемое значение счётчика — целое число в диапазоне 0–`cap`,
 *   равное `min(max(0, ⌊actualCount⌋), cap)`.
 */
export function saturateMessageCount(actualCount: number, cap: number): number {
  // Нормализуем потолок: он не может быть отрицательным.
  const ceiling = Number.isFinite(cap) ? Math.max(0, Math.trunc(cap)) : 0;
  if (!Number.isFinite(actualCount)) {
    // Бесконечность/NaN трактуем как «очень много» → насыщение на потолке.
    return Number.isNaN(actualCount) ? 0 : ceiling;
  }
  const normalized = Math.max(0, Math.trunc(actualCount));
  return Math.min(normalized, ceiling);
}
