/**
 * Типы слоя отправки электронной почты (SendPulse).
 *
 * Абстрагируют сообщение и адаптер отправки, чтобы фактический HTTP-вызов
 * внешнего сервиса можно было подменить в тестах без реальных учётных данных
 * (Req 1.6, 1.7).
 */

/** Сообщение электронной почты, передаваемое на отправку. */
export interface EmailMessage {
  /** Адрес получателя. */
  to: string;
  /** Тема письма. */
  subject: string;
  /** HTML-тело письма. */
  html: string;
  /** Необязательное текстовое тело (fallback для клиентов без HTML). */
  text?: string;
}

/**
 * Абстракция адаптера отправки почты.
 *
 * Скрывает конкретную реализацию (SendPulse HTTP API) за интерфейсом, что
 * позволяет инъецировать мок в тестах. Метод {@link MailerProvider.send}
 * выполняет ровно одну попытку сетевого вызова и бросает исключение при ошибке
 * или недоступности сервиса; повторные попытки выполняет воркер очереди
 * (Req 1.6, 1.7).
 */
export interface MailerProvider {
  /**
   * Отправляет письмо немедленно (один сетевой вызов).
   * @throws Error при ошибке отправки либо недоступности сервиса.
   */
  send(message: EmailMessage): Promise<void>;
}
