import { personaRoom } from '../auth';

/**
 * Имена комнат Socket.IO для realtime-чата (Req 11.1).
 *
 * Используются два вида комнат:
 * - комната Задачи ({@link taskRoom}) — объединяет подключения Участников чата
 *   конкретной Задачи для доставки Сообщений, обновлений Статуса и счётчиков;
 * - персональная комната Пользователя ({@link personaRoom}, реэкспорт из
 *   AuthModule) — объединяет все подключения одного Пользователя для адресной
 *   доставки уведомлений и принудительного разрыва сессий (Req 3.4, 8.6,
 *   19.10).
 *
 * Имя персональной комнаты определено в AuthModule как единый источник истины,
 * чтобы {@link SocketSessionDisconnector} и ChatGateway согласованно
 * адресовали один и тот же набор сокетов.
 */

/**
 * Имя комнаты Задачи.
 *
 * @param taskId Идентификатор Задачи.
 * @returns Имя комнаты вида `task:{taskId}`.
 */
export function taskRoom(taskId: string): string {
  return `task:${taskId}`;
}

export { personaRoom };
