/**
 * Имена событий Socket.IO realtime-чата (Req 11.1, 11.3, 9.7).
 *
 * Входящие события (от клиента к серверу) обрабатываются подписчиками
 * ChatGateway; исходящие (от сервера к клиенту) рассылаются в комнаты Задачи
 * или персональные комнаты Пользователей. Имена вынесены в константы, чтобы
 * исключить расхождение строковых литералов между сервером и клиентом.
 */
export const ChatEvents = {
  /** Входящее: запрос на вход в комнату Задачи. Полезная нагрузка `{ taskId }`. */
  TaskJoin: 'task:join',
  /** Входящее: запрос на выход из комнаты Задачи. Полезная нагрузка `{ taskId }`. */
  TaskLeave: 'task:leave',
  /** Исходящее: новое/изменённое Сообщение Чата (Req 11.3). */
  Message: 'chat:message',
  /** Исходящее: обновление Статуса Задачи (Req 10, live-обновление). */
  StatusUpdate: 'task:status',
  /** Исходящее: обновление счётчика Сообщений на карточке Задачи (Req 9.7). */
  MessageCounter: 'task:counter',
  /** Исходящее: обновлённый список прочитавших Сообщение Участников (Req 11.8). */
  MessageReaders: 'chat:reads',
  /** Исходящее: адресное уведомление Пользователю в его персональной комнате (Req 13, 14). */
  Notification: 'notification',
} as const;

/** Тип значения из {@link ChatEvents}. */
export type ChatEvent = (typeof ChatEvents)[keyof typeof ChatEvents];
